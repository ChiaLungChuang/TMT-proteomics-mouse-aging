# Returns the threshold list for a given organ × comparison combination
get_thresholds <- function(organ_code, comparison) {
  if (organ_code %in% names(thresholds_config) &&
      comparison %in% names(thresholds_config[[organ_code]])) {
    return(thresholds_config[[organ_code]][[comparison]])
  } else if (comparison %in% names(thresholds_config$default)) {
    return(thresholds_config$default[[comparison]])
  }
  return(list(soluble_high = 1.2, insoluble_high = 1.0, soluble_low = -1.0, insoluble_low = -0.8))
}

