load_combined_all <- function() {
  organ_full <- c(TA="Tibialis Anterior", He="Heart", CB="Cerebellum",
                  HC="Hippocampus", NC="Neocortex", So="Soleus",
                  Li="Liver", WAT="White Adipose Tissue")
  
  bind_rows(lapply(names(organ_full), function(code) {
    f <- paste0("combined_plot_data_mouseTMT_", code, ".csv")
    if (!file.exists(f)) return(NULL)
    read.csv(f) %>% mutate(Organ = organ_full[code], is_significant = !is.na(Size) & Size >= 1.3,
                           Organ_Name = Organ)
  })) %>%
    mutate(Comparison = factor(Comparison, levels = c("Old vs. Young", "Middle vs. Young", "Old vs. Middle")))
}

