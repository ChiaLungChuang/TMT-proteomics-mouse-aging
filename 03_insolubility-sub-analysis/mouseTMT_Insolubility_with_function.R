# ============================================================
# 3. Main Per-Tissue Processing Function
# ============================================================

process_organ_data <- function(organ_code, file_path) {
  cat("\n====", organ_code, "====\n")
  
  # --- 3a. Data import and cleaning ---
  data <- read_excel(file_path)
  write.csv(data, paste0("FD_", organ_code, ".csv"), row.names = FALSE)
  data_org <- read.csv(paste0("FD_", organ_code, ".csv")) %>%
    mutate(Protein.ID_cleaned = sub("_DROME$", "", Protein.Accession..),
           GN_ID = paste(GN, Protein.ID_cleaned, sep = "_"))
  write.csv(data_org, paste0("data_mouse_", organ_code, ".csv"), row.names = FALSE)
  
  # --- 3b. Reshape to long format ---
  normalize_replicates <- function(df) {
    df %>%
      group_by(Age, Solubility) %>%
      mutate(Replicate = as.integer((dense_rank(Replicate) - 1) %% 3 + 1)) %>%
      ungroup()
  }
  
  data_org_long <- data_org %>%
    pivot_longer(cols = starts_with("sig"),
                 names_to = c("Age", "Replicate", "Solubility"),
                 names_pattern = pattern_map[[organ_code]],
                 values_to = "Measurement") %>%
    mutate(Age = case_when(Age == "6M" ~ "Young", Age == "24M" ~ "Middle", Age == "30M" ~ "Old"),
           Solubility = case_when(Solubility == "S" ~ "Soluble", Solubility == "I" ~ "Insoluble"),
           Replicate = as.integer(Replicate), Measurement = as.numeric(Measurement)) %>%
    normalize_replicates()
  
  data_org_wider <- data_org_long %>%
    pivot_wider(names_from = Solubility, values_from = Measurement,
                id_cols = c(Age, GN, GN_ID, Replicate)) %>%
    mutate(Ratio = Insoluble / Soluble, Abundance = Soluble + Insoluble)
  
  # --- 3c. ANOVA + Tukey HSD for Ratio, Abundance, Soluble, Insoluble ---
  run_tukey <- function(df, response) {
    formula <- as.formula(paste(response, "~ Age"))
    anova_res <- df %>% group_by(GN_ID) %>%
      do(aov_result = aov(formula, data = .)) %>% ungroup()
    
    tukey_res <- anova_res %>%
      mutate(tukey_result = map(aov_result, ~ tidy(TukeyHSD(.)))) %>%
      unnest(tukey_result) %>%
      select(GN_ID, comparison = contrast, p.value = adj.p.value) %>%
      pivot_wider(names_from = comparison, values_from = p.value, names_prefix = "p.value_") %>%
      rename_with(~ gsub("p.value_Young-Middle", paste0(substr(response,1,2), ".p.value_middle_young"), .),
                  starts_with("p.value_Young-Middle")) %>%
      rename_with(~ gsub("p.value_Old-Middle",  paste0(substr(response,1,2), ".p.value_old_middle"),   .),
                  starts_with("p.value_Old-Middle")) %>%
      rename_with(~ gsub("p.value_Young-Old",   paste0(substr(response,1,2), ".p.value_old_young"),    .),
                  starts_with("p.value_Young-Old"))
    return(tukey_res)
  }
  
  tukey_ra  <- run_tukey(data_org_wider, "Ratio")
  tukey_ab  <- run_tukey(data_org_wider, "Abundance")
  tukey_so  <- run_tukey(data_org_wider, "Soluble")
  tukey_ins <- run_tukey(data_org_wider, "Insoluble")
  
  # --- 3d. Log2 fold changes for all 3 comparisons ---
  compute_fc <- function(df, a, b, suffix) {
    df %>%
      group_by(GN_ID, GN) %>%
      summarize(
        "{suffix}_Abundance_mean_{a}" := mean(Abundance[Age == tools::toTitleCase(a)], na.rm = TRUE),
        "{suffix}_Abundance_mean_{b}" := mean(Abundance[Age == tools::toTitleCase(b)], na.rm = TRUE),
        "{suffix}_Ratio_mean_{a}"     := mean(Ratio[Age == tools::toTitleCase(a)], na.rm = TRUE),
        "{suffix}_Ratio_mean_{b}"     := mean(Ratio[Age == tools::toTitleCase(b)], na.rm = TRUE),
        "{suffix}_Soluble_mean_{a}"   := mean(Soluble[Age == tools::toTitleCase(a)], na.rm = TRUE),
        "{suffix}_Soluble_mean_{b}"   := mean(Soluble[Age == tools::toTitleCase(b)], na.rm = TRUE),
        "{suffix}_Insoluble_mean_{a}" := mean(Insoluble[Age == tools::toTitleCase(a)], na.rm = TRUE),
        "{suffix}_Insoluble_mean_{b}" := mean(Insoluble[Age == tools::toTitleCase(b)], na.rm = TRUE),
        .groups = "drop"
      )
  }
  
  # Use simpler direct approach for clarity
  fc_OY <- data_org_wider %>% group_by(GN_ID, GN) %>%
    summarize(Abundance_FC_old_young  = log2(mean(Abundance[Age=="Old"],   na.rm=T) / mean(Abundance[Age=="Young"], na.rm=T)),
              Ratio_FC_old_young      = log2(mean(Ratio[Age=="Old"],       na.rm=T) / mean(Ratio[Age=="Young"],     na.rm=T)),
              Soluble_FC_old_young    = log2(mean(Soluble[Age=="Old"],     na.rm=T) / mean(Soluble[Age=="Young"],   na.rm=T)),
              Insoluble_FC_old_young  = log2(mean(Insoluble[Age=="Old"],   na.rm=T) / mean(Insoluble[Age=="Young"], na.rm=T)),
              Abundance_mean_young    = mean(Abundance[Age=="Young"], na.rm=T), .groups="drop")
  
  fc_MY <- data_org_wider %>% group_by(GN_ID, GN) %>%
    summarize(Abundance_FC_middle_young  = log2(mean(Abundance[Age=="Middle"],  na.rm=T) / mean(Abundance[Age=="Young"], na.rm=T)),
              Ratio_FC_middle_young      = log2(mean(Ratio[Age=="Middle"],      na.rm=T) / mean(Ratio[Age=="Young"],     na.rm=T)),
              Soluble_FC_middle_young    = log2(mean(Soluble[Age=="Middle"],    na.rm=T) / mean(Soluble[Age=="Young"],   na.rm=T)),
              Insoluble_FC_middle_young  = log2(mean(Insoluble[Age=="Middle"],  na.rm=T) / mean(Insoluble[Age=="Young"], na.rm=T)),
              .groups = "drop")
  
  fc_OM <- data_org_wider %>% group_by(GN_ID, GN) %>%
    summarize(Abundance_FC_old_middle  = log2(mean(Abundance[Age=="Old"],   na.rm=T) / mean(Abundance[Age=="Middle"], na.rm=T)),
              Ratio_FC_old_middle      = log2(mean(Ratio[Age=="Old"],       na.rm=T) / mean(Ratio[Age=="Middle"],     na.rm=T)),
              Soluble_FC_old_middle    = log2(mean(Soluble[Age=="Old"],     na.rm=T) / mean(Soluble[Age=="Middle"],   na.rm=T)),
              Insoluble_FC_old_middle  = log2(mean(Insoluble[Age=="Old"],   na.rm=T) / mean(Insoluble[Age=="Middle"], na.rm=T)),
              .groups = "drop")
  
  write.csv(fc_OY, paste0("ratio_data_", organ_code, "_OY.csv"), row.names = FALSE)
  write.csv(fc_MY, paste0("ratio_data_", organ_code, "_MY.csv"), row.names = FALSE)
  write.csv(fc_OM, paste0("ratio_data_", organ_code, "_OM.csv"), row.names = FALSE)
  
  # --- 3e. Merge and build plot_data ---
  plot_data_org <- fc_OY %>%
    left_join(fc_OM, by = c("GN_ID","GN")) %>%
    left_join(fc_MY, by = c("GN_ID","GN")) %>%
    left_join(tukey_ra,  by = "GN_ID") %>%
    left_join(tukey_ab,  by = "GN_ID") %>%
    left_join(tukey_so,  by = "GN_ID") %>%
    left_join(tukey_ins, by = "GN_ID") %>%
    mutate(
      Size_ra_old_young    = ifelse(is.na(Ra.p.value_old_young),    1, -log10(Ra.p.value_old_young)),
      Size_ra_middle_young = ifelse(is.na(Ra.p.value_middle_young), 1, -log10(Ra.p.value_middle_young)),
      Size_ra_old_middle   = ifelse(is.na(Ra.p.value_old_middle),   1, -log10(Ra.p.value_old_middle))
    ) %>%
    filter(!grepl("co|CON_", GN_ID))
  
  write.csv(plot_data_org, paste0("mouseTMT_", organ_code, ".csv"), row.names = FALSE)
  
  # --- 3f. Build per-comparison combined tables ---
  make_comp_table <- function(fc_col_suffix, size_col, comp_label) {
    plot_data_org %>%
      mutate(Comparison = comp_label) %>%
      select(GN_ID, GN,
             Abundance_FC    = paste0("Abundance_FC_", fc_col_suffix),
             Ratio_FC        = paste0("Ratio_FC_",     fc_col_suffix),
             Soluble_FC      = paste0("Soluble_FC_",   fc_col_suffix),
             Insoluble_FC    = paste0("Insoluble_FC_", fc_col_suffix),
             Size            = !!sym(size_col),
             Comparison)
  }
  
  comp_OY <- make_comp_table("old_young",    "Size_ra_old_young",    "Old vs. Young")
  comp_MY <- make_comp_table("middle_young", "Size_ra_middle_young", "Middle vs. Young")
  comp_OM <- make_comp_table("old_middle",   "Size_ra_old_middle",   "Old vs. Middle")
  
  # Apply labeling thresholds
  add_labels <- function(df, comp) {
    t <- get_thresholds(organ_code, comp)
    df %>% mutate(Label = case_when(
      is.na(GN) | GN == "NA" ~ NA_character_,
      Soluble_FC > t$soluble_high | Insoluble_FC > t$insoluble_high |
        Soluble_FC < t$soluble_low  | Insoluble_FC < t$insoluble_low ~ GN,
      TRUE ~ NA_character_
    ))
  }
  
  combined_org <- bind_rows(
    add_labels(comp_OY, "Old vs. Young"),
    add_labels(comp_MY, "Middle vs. Young"),
    add_labels(comp_OM, "Old vs. Middle")
  ) %>% filter(!is.na(GN) & GN != "NA")
  
  # Grey variant — significance flag for background points
  combined_org_grey <- combined_org %>%
    mutate(is_significant = !is.na(Size) & Size >= 1.3)
  
  write.csv(combined_org %>% select(-Label),        paste0("combined_plot_data_mouseTMT_", organ_code, ".csv"),        row.names = FALSE)
  write.csv(combined_org_grey %>% select(-Label),   paste0("combined_plot_data_mouseTMT_", organ_code, "_grey.csv"),   row.names = FALSE)
  write.csv(combined_org %>% filter(!is.na(Size) & Size >= 1.3) %>% select(-Label),
            paste0("combined_plot_data_mouseTMT_", organ_code, "_filtered.csv"), row.names = FALSE)
  
  # --- 3g. Visualization ---
  full_name <- organ_name_map[organ_code]
  
  shared_theme <- theme_minimal() +
    theme(panel.spacing = unit(1.5, "lines"),
          axis.text.x   = element_text(size = 14, face = "bold"),
          axis.text.y   = element_text(size = 14, face = "bold"),
          legend.text   = element_text(size = 12, face = "italic"),
          legend.title  = element_text(size = 14, face = "bold"),
          plot.title    = element_text(size = 16, face = "bold"),
          axis.title.x  = element_text(size = 14, face = "bold"),
          axis.title.y  = element_text(size = 14, face = "bold"),
          strip.text    = element_text(size = 14, face = "bold"))
  
  comp_colors <- scale_color_manual(
    values = c("Old vs. Young" = "purple4", "Middle vs. Young" = "maroon2", "Old vs. Middle" = "darkgreen"))
  
  filtered_org <- combined_org %>% filter(!is.na(Size) & Size >= 1.3)
  
  # Colored — significant only
  p_colored <- ggplot(filtered_org, aes(x = Soluble_FC, y = Insoluble_FC, size = Size,
                                        label = Label, color = Comparison)) +
    geom_point(alpha = 0.4) +
    geom_text_repel(size = 5, fontface = "bold", segment.color = "grey50",
                    max.overlaps = Inf, nudge_x = 0.1, nudge_y = 0.1,
                    box.padding = 0.2, point.padding = 0.2, na.rm = TRUE) +
    scale_size_continuous(range = c(1,9), name = "P-Value Size (-log10)",
                          breaks = c(1.3,2,3,4,5), limit = c(1.3,6),
                          labels = c("1.30(p<0.05)","2","3","4","5")) +
    comp_colors +
    stat_poly_eq(aes(label = paste(after_stat(rr.label))), rr.digits = 3,
                 parse = TRUE, color = "black", show.legend = FALSE) +
    labs(title   = paste0("MouseTMT ", full_name, ": Soluble FC vs. Insoluble FC"),
         x       = "Log2 Soluble fold change",
         y       = "Log2 Insoluble fold change",
         caption = "Point size based on statistical significance of Age difference in insolubility.") +
    scale_x_continuous(labels = scales::label_number(accuracy = 0.1)) +
    shared_theme + facet_wrap(~Comparison) +
    guides(color = guide_legend(override.aes = list(label = "")))
  
  print(p_colored)
  ggsave(paste0("mouseTMT_", organ_code, "_combined_facet_label.pdf"),
         p_colored, width = 7200, height = 6400, units = "px", dpi = 600)
  
  # Grey background — non-significant points as small grey dots
  p_grey <- ggplot() +
    geom_point(data = combined_org_grey %>% filter(!is_significant),
               aes(x = Soluble_FC, y = Insoluble_FC), size = 0.5, color = "gray60", alpha = 0.4) +
    geom_point(data = combined_org_grey %>% filter(is_significant),
               aes(x = Soluble_FC, y = Insoluble_FC, size = Size, color = Comparison), alpha = 0.4) +
    comp_colors +
    geom_text_repel(data = combined_org_grey %>% filter(is_significant),
                    aes(x = Soluble_FC, y = Insoluble_FC, label = Label, color = Comparison),
                    size = 5, fontface = "bold", segment.color = "grey50",
                    max.overlaps = Inf, nudge_x = 0.1, nudge_y = 0.1,
                    box.padding = 0.2, point.padding = 0.2, na.rm = TRUE) +
    scale_size_continuous(range = c(1,9), name = "P-Value Size (-log10)",
                          breaks = c(1.3,2,3,4,5), limit = c(1.3,8),
                          labels = c("1.30(p<0.05)","2","3","4","5")) +
    stat_poly_eq(data = combined_org_grey, aes(x = Soluble_FC, y = Insoluble_FC,
                                               label = paste(after_stat(rr.label))), rr.digits = 3,
                 parse = TRUE, color = "black", show.legend = FALSE, inherit.aes = FALSE) +
    labs(title   = paste0("MouseTMT ", full_name, ": Soluble FC vs. Insoluble FC"),
         x       = "Log2 Soluble fold change",
         y       = "Log2 Insoluble fold change",
         caption = "Colored: significant (p < 0.05). Grey: non-significant.") +
    scale_x_continuous(labels = scales::label_number(accuracy = 0.1)) +
