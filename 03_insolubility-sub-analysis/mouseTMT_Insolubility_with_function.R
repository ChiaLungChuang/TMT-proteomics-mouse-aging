# Script:  mouseTMT_insolubility_with_function.R
# Purpose: Modular reanalysis pipeline — re-runs the full
#          per-tissue analysis (normalization → ANOVA + Tukey
#          HSD → fold change → visualization) using a single
#          process_organ_data() function with organ-specific
#          labeling thresholds. Produces Soluble FC vs.
