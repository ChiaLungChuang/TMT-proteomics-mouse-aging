    guides(color = guide_legend(override.aes = list(label = "")))
  
  print(p_grey)
  ggsave(paste0("mouseTMT_", organ_code, "_combined_facet_label_grey.pdf"),
         p_grey, width = 7200, height = 6400, units = "px", dpi = 600)
  
  return(list(combined_data = combined_org, combined_data_grey = combined_org_grey,
