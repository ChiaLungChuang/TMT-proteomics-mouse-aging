#          ratio_data_<organ>_OY/MY/OM.csv,
#          mouseTMT_<organ>_combined_facet_label*.pdf,
#          mouseTMT_multi_organ_soluble_vs_ratio*.pdf,
#          pearson_correlations_soluble_vs_ratio*.csv
# Author:  Chia-Lung Chuang
