# All proteins
correlations_all <- bind_rows(lapply(names(all_organs_data), function(code) {
  d <- all_organs_data[[code]]$combined_data %>%
    filter(!is.na(Soluble_FC), !is.na(Ratio_FC), is.finite(Soluble_FC), is.finite(Ratio_FC))
  
  bind_rows(lapply(unique(d$Comparison), function(comp) {
    sub <- d %>% filter(Comparison == comp)
    if (nrow(sub) <= 2) return(NULL)
    ct <- cor.test(sub$Soluble_FC, sub$Ratio_FC, method = "pearson")
    data.frame(Organ = code, Comparison = comp,
               Pearson_r = ct$estimate, p_value = ct$p.value, n = nrow(sub))
  }))
}))

write.csv(correlations_all, "pearson_correlations_soluble_vs_ratio.csv", row.names = FALSE)
cat("\nPearson correlations (all proteins):\n")
print(correlations_all)

